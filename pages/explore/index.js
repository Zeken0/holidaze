import React from "react";
import Head from "next/head";
import Link from "next/link";
import Image from "next/image";
import NavBar from "../../components/NavBar";
import Footer from "../../components/Footer";
import styles from "../../styles/Home.module.scss";
import {
  Select,
  Checkbox,
  RangeSlider,
  RadioGroup,
  Radio,
} from "@mantine/core";
import { Search, Location } from "tabler-icons-react";
import { useRouter } from "next/router";
import axios from "axios";

export async function getStaticProps() {
  let hotels = [];
  try {
    const response = await axios.get("http://localhost:1337/api/hotels");
    const data = await response.data;
    hotels = data.data;
  } catch (error) {
    console.log(error);
  }
  return {
    props: {
      hotels: hotels,
    },
  };
}

function Explore({ hotels }) {
  const router = useRouter();

  const handleOnClick = (hotelName) => {
    if (!hotelName) {
      return;
    }

    const selectedHotel = hotels.find((currentHotel) => {
      return currentHotel.attributes.name === hotelName;
    });

    router.push(`/explore/${selectedHotel.id}`);
  };

  const searchData = hotels.map((hotel) => {
    return hotel.attributes.name;
  });
  return (
    <div>
      <Head>
        <title>Explore | Holidaze</title>
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <NavBar />
      <main className={styles.explore_container}>
        <div className={styles.explore_actions}>
          <h1>Explore our bookings</h1>
          <Select
            placeholder="Search for hotels"
            variant="filled"
            size="lg"
            nothingFound="No hotels found"
            icon={<Search size={21} />}
            searchable
            clearable
            transitionDuration={100}
            transitionTimingFunction="ease"
            maxDropdownHeight={210}
            data={searchData}
            onChange={(hotel) => {
              handleOnClick(hotel);
            }}
          />
        </div>
        <div className={styles.explore_hotels_container}>
          <div className={styles.explore_filter}>
            <div>
              <h3>Popular filters</h3>
              <Checkbox
                label="Breakfast included"
                color="red"
                radius="xs"
                size="md"
              />
              <Checkbox label="Spa" color="red" radius="xs" size="md" />
              <Checkbox label="Pool" color="red" radius="xs" size="md" />
              <Checkbox
                label="Pet friendly"
                color="red"
                radius="xs"
                size="md"
              />
              <Checkbox
                label="Bathtub in room"
                color="red"
                radius="xs"
                size="md"
              />
            </div>
            <div>
              <h3>Price (for 1 night)</h3>
              <RangeSlider
                color="red"
                size="lg"
                showLabelOnHover={false}
                marks={[
                  { value: 0, label: "cheap" },
                  { value: 50, label: "affordable" },
                  { value: 100, label: "expensive" },
                ]}
              />
            </div>
            <div>
              <h3>Star rating</h3>
              <RadioGroup spacing="xs" size="md" color="red">
                <Radio value="One" label="One" />
                <Radio value="Two" label="Two" />
                <Radio value="Three" label="Three" />
                <Radio value="Foure" label="Four" />
              </RadioGroup>
            </div>
          </div>
          <div className={styles.explore_content}>
            {hotels.map((hotel) => {
              return (
                <div className={styles.content_item} key={hotel.id}>
                  <Image
                    src="/images/emailBanner.png"
                    height={250}
                    width={250}
                    alt="Image of a hotel"
                  />
                  <div className={styles.item_info}>
                    <Link href={"/explore/" + hotel.id} key={hotel.id}>
                      <h3>{hotel.attributes.name}</h3>
                    </Link>
                    <span>
                      <Location /> {hotel.attributes.location}
                    </span>
                    <Link href={"/explore/" + hotel.id} key={hotel.id}>
                      <button className={styles.item_button}>View more</button>
                    </Link>
                  </div>
                </div>
              );
            })}
          </div>
        </div>
      </main>
      <Footer />
    </div>
  );
}

export default Explore;
